/** Filtre par niveau JLPT */
export const getByJlpt = (level: VocabItem["jlpt"]): VocabItem[] =>
  japaneseVocab.filter((item) => item.jlpt === level);

/** Filtre par catégorie */
export const getByCategory = (category: string): VocabItem[] =>
  japaneseVocab.filter((item) => item.category === category);

/** Filtre par partie du discours */
export const getByPos = (pos: string): VocabItem[] =>
  japaneseVocab.filter((item) => item.pos === pos);

/** Retourne un sous-ensemble aléatoire de N éléments */
export const getRandomVocab = (n: number): VocabItem[] => {
  const shuffled = [...japaneseVocab].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
};

/** Recherche full-text sur japanese, kana, romaji et french */
export const searchVocab = (query: string): VocabItem[] => {
  const q = query.toLowerCase();
  return japaneseVocab.filter(
    (item) =>
      item.japanese.includes(query) ||
      item.kana.includes(query) ||
      item.romaji.toLowerCase().includes(q) ||
      item.french.toLowerCase().includes(q)
  );
};

/** Récupère les N mots les plus fréquents */
export const getTopFrequency = (n: number): VocabItem[] =>
  [...japaneseVocab].sort((a, b) => a.frequencyRank - b.frequencyRank).slice(0, n);

/** Liste de toutes les catégories uniques */
export const allCategories: string[] = [
  ...new Set(japaneseVocab.map((item) => item.category)),
];

/** Nombre total de mots dans le fichier */
export const VOCAB_COUNT = japaneseVocab.length;
